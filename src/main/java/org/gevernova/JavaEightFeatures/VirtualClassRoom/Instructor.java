package org.gevernova.JavaEightFeatures.VirtualClassRoom;

public class Instructor {

    // Unique identifier for the instructor
    int id;

    // Name of the instructor
    String name;

    /**
     * Constructor to initialize instructor details.
     *
     * @param id   Unique instructor ID
     * @param name Name of the instructor
     */
    public Instructor(int id, String name) {
        this.id = id;
        this.name = name;
    }
}
